# empty list
my_list = []

# list of integers
my_list = [1, 2, 3]

# list with mixed datatypes
my_list = [1, "Hello", 3.4]
#Also, a list can even have another list as an item. This is called nested list.

# nested list
my_list = ["mouse", [8, 4, 6], ['a']]

print(len(my_list[0]))

my_list = ['p','r','o','g','r','a','m','i','z']

print(my_list[2:5])

print(my_list[5:])

print(my_list[:-5])

print(my_list[:])
